SELECT ENAME,DEPTNO
FROM EMP
WHERE DEPTNO=30;
/*WAQTD NAME&SALARY OF EMPLOYEE IF EMPLOYEE
IS GETTING SALARY MORE THAN 2500*/
SELECT ENAME,SAL
FROM EMP
WHERE SAL>2500;
/*WAQTD NAME &JOB OF EMPLOYEE IF EMPLOYEE
IS WORKING AS MANAGER*/
SELECT *
FROM EMP;
SELECT ENAME,JOB
FROM EMP
WHERE JOB="MANAGER";
/*WAQTD DETAILS OF EMPLOYEE IF EMPLOYEE
  GETTING A COMMISSION RS 1400*/

   4)RELATIONAL OPERATORS(<,>,<=,>=)
   5)LOGICAL OPERATORS(AND,OR,NOT)
   6)SPECIAL OPERATORS
   (IN,NOT IN,BETWEEN,NOT BETWEEN,IS,IS NOT,
    LIKE,NOT LIKE)
7)SUBQUERY OPERATOR(ALL,ANY,EXISTS,NOT EXISTS)
/*LOGICAL OPERATORS:
"TO WRITE MULTIPLE CONDITIONS IN WHERE
 CLASUE WE USE LOGICAL OPERATOR.&OUTPUT
 OF LOGICAL OPERATOR IS ALWAYS A BOOLEAN 
 VALUE(TRUE/FALSE).
/*AND:
